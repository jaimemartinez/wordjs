/**
 * Get translation for a key
 */
export function t(key: string, lang: Language = 'es'): string {
    return translations[lang][key] || translations['en'][key] || key;
}

/**
 * Register new translations (used by plugins)
 */
export function registerTranslations(newTranslations: Record<string, Record<string, string>>) {
    Object.keys(newTranslations).forEach((lang) => {
        // Ensure the language exists in our global map (fallback to empty object if not, though we stick to supported langs)
        if (translations[lang as Language]) {
            Object.assign(translations[lang as Language], newTranslations[lang]);
        }
    });
}

